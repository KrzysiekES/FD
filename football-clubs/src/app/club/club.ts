export class Club {

    name: string;
    city: string;
    coach: string;
    ground: string;
    league: string;
    capacity: number;

    constructor(name, city, coach, ground, league, capacity){
        this.name = name;
        this.city = city;
        this.coach = coach;
        this.ground = ground;
        this.league = league;
        this.capacity = capacity;
    }
    
}
